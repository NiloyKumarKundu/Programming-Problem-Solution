int extendedEuclidean(int A, int B, int *X, int *Y) {
	int x, y, x1, y1, x2, y2, q, r, r1, r2;
	x1 = 0, y1 = 1;
	x2 = 1, y2 = 0;

	for (r2 = A, r1 = B; r1 != 0; r2 = r1, r1 = r, x2 = x1, y2 = y1, x1 = x, y1 = y) {
		q = r2 / r1;
		r = r2 % r1;
		x = x2 - (q * x1);
		y = y2 - (q * y1);
	}
	*X = x2;
	*Y = y2;
	return r2;
}

